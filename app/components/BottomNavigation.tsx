export function BottomNavigation() {
  const location = useLocation();
  const { uiSettings } = useUI();

  if (!uiSettings.showNavigation) {
    return null;
  }

  return (
    <nav 
      className="bottom-navigation fixed bottom-0 left-0 right-0 px-4 py-2 z-50 border-t"
      style={{ 
        backgroundColor: 'var(--color-surface)', 
        borderColor: 'var(--color-border)' 
      }}
    >
      <div className="flex justify-around items-center max-w-md mx-auto">
        {navigationItems.map((item) => {
          const isActive = location.pathname === item.href;
          return (
            <Link
              key={item.name}
              to={item.href}
              className="flex flex-col items-center py-2 px-3 rounded-lg transition-colors"
              style={{
                backgroundColor: isActive ? 'var(--color-accent)' : 'transparent',
                '--tw-bg-opacity': isActive ? '1' : '0'
              } as React.CSSProperties & { '--tw-bg-opacity': string }}
            >
              <span style={{ color: isActive ? 'white' : 'var(--color-text-tertiary)' }}>
                {item.icon}
              </span>
              <span 
                className="text-xs mt-1 font-medium"
                style={{ color: isActive ? 'white' : 'var(--color-text-secondary)' }}
              >
                {item.name}
              </span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
